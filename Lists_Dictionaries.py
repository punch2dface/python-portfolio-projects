# Lists and Dictionaries in Python

# Creating a list
fruits = ["Apple", "Banana", "Orange"]

# Creating a dictionary
person = {"name": "Ben", "job": "student", "likes": ["games", "tech"]}

# Accessing Lists : Output Banana
print(fruits[1])

# Accessing Dictionary : Output the person's likes list
print(person["likes"])

# Adding an item to the list
fruits.append("Grapes")

# Output the updated list
print(fruits)