# Loops in Python

# looping through a range of numbers and print each number
for i in range(5):
    print("Loop:", i)
    
# looping through a list of fruits and print each fruit
fruits = ["Apple", "Banana", "Orange"]

for fruit in fruits:
    print(fruit)

# while loop that counts down from 5 to 1
count = 5
while count > 0:
    print(count)
    count -= 1
    
